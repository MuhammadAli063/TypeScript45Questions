// Function Make a two numbers sum
function add(a:number,b:number):number{
    return a+b;
}
// Function Make a two numbers subtraction
function sub(c:number,d:number):number{
    return c-d;
}

// Addition
console.log("Addition of Two Numbers : " + add(5,5));
// Subtraction
console.log("Subtraction of Two Numbers : " + sub(20,10));